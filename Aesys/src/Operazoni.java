public class Operazoni {
    public static int Somma(int a, int b){
        return a+b;
    }

    public static int Sottrazione(int a, int b){
        return a-b;
    }

    public static int Moltiplicazione(int a, int b){
        return a*b;
    }

    public static float Divisione(int a, int b){
        return a/b;
    }
}
